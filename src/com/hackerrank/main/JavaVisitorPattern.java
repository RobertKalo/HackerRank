package com.hackerrank.main;

import java.util.ArrayList;
import java.util.LinkedList;
import java.util.Scanner;

enum Color {
	RED, GREEN
}

abstract class Tree {

	private int		value;
	private Color	color;
	private int		depth;

	public Tree(int value, Color color, int depth) {
		this.value = value;
		this.color = color;
		this.depth = depth;
	}

	public int getValue() {
		return value;
	}

	public Color getColor() {
		return color;
	}

	public int getDepth() {
		return depth;
	}

	public abstract void accept( TreeVis visitor );
}

class TreeNode extends Tree {

	private ArrayList<Tree> children = new ArrayList<>();

	public TreeNode(int value, Color color, int depth) {
		super( value, color, depth );
	}

	public void accept( TreeVis visitor ) {
		visitor.visitNode( this );

		for ( Tree child : children ) {
			child.accept( visitor );
		}
	}

	public void addChild( Tree child ) {
		children.add( child );
	}
}

class TreeLeaf extends Tree {

	public TreeLeaf(int value, Color color, int depth) {
		super( value, color, depth );
	}

	public void accept( TreeVis visitor ) {
		visitor.visitLeaf( this );
	}
}

abstract class TreeVis {
	public abstract int getResult();

	public abstract void visitNode( TreeNode node );

	public abstract void visitLeaf( TreeLeaf leaf );

}

class SumInLeavesVisitor extends TreeVis {

	private int sum;

	public int getResult() {
		return sum;
	}

	public void visitNode( TreeNode node ) {
		if ( ( (TreeElement) node ).isLeaf() ) {
			sum += node.getValue();
		}
	}

	public void visitLeaf( TreeLeaf leaf ) {
	}
}

class ProductOfRedNodesVisitor extends TreeVis {
	private int	prod	= 1;
	final int	M		= 1000000007;

	public int getResult() {
		return prod == 0 ? 1 : prod;
	}

	public void visitNode( TreeNode node ) {
		if ( !( (TreeElement) node ).isLeaf() && node.getColor() == Color.RED ) {
			int v = node.getValue();
			prod = ( ( prod * v ) % M );
		}
	}

	public void visitLeaf( TreeLeaf leaf ) {
	}
}

class FancyVisitor extends TreeVis {
    private int sum1 = 0;
    private int sum2 = 0;
    public int getResult() {
        return Math.abs(sum1 - sum2);
    }

    public void visitNode(TreeNode node) {

        if (!((TreeElement)node).isLeaf() && node.getDepth() % 2 == 0)
            sum1 += node.getValue();
        else if (((TreeElement)node).isLeaf() && node.getColor() == Color.GREEN)
            sum2 += node.getValue();
    }

    public void visitLeaf(TreeLeaf leaf) {
    }
}

class TreeElement extends TreeNode {

	private boolean _hasChild = false;
    public TreeElement(int value, Color color, int depth) {
        super(value, color, depth);
    }
    public boolean isLeaf() {
        return !_hasChild;
    }
    @Override
    public void addChild(Tree child) {
        _hasChild = true;
        super.addChild(child);
    }
}

public class JavaVisitorPattern {

	public static Tree solve() {

		Scanner in = new Scanner( System.in );
		int n = in.nextInt();

		int[] value = new int[n];
		for ( int i = 0; i < n; i++ ) {
			value[i] = in.nextInt();
		}

		Color[] color = new Color[n];
		for ( int i = 0; i < n; i++ ) {
			color[i] = in.nextInt() == 0 ? Color.RED : Color.GREEN;
		}

		TreeElement[] treeElement = new TreeElement[n];
		treeElement[0] = new TreeElement( value[0], color[0], 0 );

		LinkedList<int[]> stack = new LinkedList<>();
		
		while ( in.hasNext() ) {
			int u = in.nextInt() - 1;
			int v = in.nextInt() - 1;
			stack.add( new int[] { u, v } );
//			if ( u < v ) {
//			} else {
//				stack.add( new int[] { v, u } );
//			}
		}

	    while (stack.size() > 0) {
	        int[] elem = stack.poll();
	        int u = elem[0]; 
	        int v = elem[1];
	        if (treeElement[u] != null && treeElement[v] == null) { // v not attached
	            treeElement[v] = new TreeElement(value[v], color[v], treeElement[u].getDepth() + 1);
	            treeElement[u].addChild(treeElement[v]);
	        }
	        else if (treeElement[u] == null && treeElement[v] != null) { // u not attached
	            treeElement[u] = new TreeElement(value[u], color[u], treeElement[v].getDepth() + 1);
	            treeElement[v].addChild(treeElement[u]);
	        }
	        else // both nodes not attached to tree; add back to stack
	            stack.add(elem);

		}

		in.close();

		return treeElement[0];
	}

	public static void main( String[] args ) {
		Tree root = solve();
		SumInLeavesVisitor vis1 = new SumInLeavesVisitor();
		ProductOfRedNodesVisitor vis2 = new ProductOfRedNodesVisitor();
		FancyVisitor vis3 = new FancyVisitor();

		root.accept( vis1 );
		root.accept( vis2 );
		root.accept( vis3 );

		int res1 = vis1.getResult();
		int res2 = vis2.getResult();
		int res3 = vis3.getResult();

		System.out.println( res1 );
		System.out.println( res2 );
		System.out.println( res3 );
	}
}
